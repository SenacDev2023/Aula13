
num1 = float(input("Digite o primeiro valor: "))
operacao = input("Informe a operação (+, -, * ou /): ")
num2 = float(input("Digite o segundo valor: "))

if operacao == "+":
    print("A soma dos valores digitados é: ", num1+num2)
elif operacao == "-":
    print("A soma dos valores digitados é: ", num1-num2)